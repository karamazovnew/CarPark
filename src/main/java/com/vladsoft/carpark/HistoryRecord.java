package com.vladsoft.carpark;

public class HistoryRecord {
	private String historyId;
	private String historyType;
	private String historyDate;
	private String historyTime;
	private String historyToken;
	private String historySection;
	private String historyPay;
	public String getHistoryId() {
		return historyId;
	}
	public void setHistoryId(String historyId) {
		this.historyId = historyId;
	}
	public String getHistoryType() {
		return historyType;
	}
	public void setHistoryType(String historyType) {
		this.historyType = historyType;
	}
	public String getHistoryDate() {
		return historyDate;
	}
	public void setHistoryDate(String historyDate) {
		this.historyDate = historyDate;
	}
	public String getHistoryTime() {
		return historyTime;
	}
	public void setHistoryTime(String historyTime) {
		this.historyTime = historyTime;
	}
	public String getHistoryToken() {
		return historyToken;
	}
	public void setHistoryToken(String historyToken) {
		this.historyToken = historyToken;
	}
	public String getHistorySection() {
		return historySection;
	}
	public void setHistorySection(String historySection) {
		this.historySection = historySection;
	}
	public String getHistoryPay() {
		return historyPay;
	}
	public void setHistoryPay(String historyPay) {
		this.historyPay = historyPay;
	}
	
	
	
}
